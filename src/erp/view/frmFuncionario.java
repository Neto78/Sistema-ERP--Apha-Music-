    public void Listar(){
        FuncionarioDAO dao = new FuncionarioDAO();
        List<Funcionario> lista = dao.listarFuncionario();
        DefaultTableModel dados = (DefaultTableModel) tabelaFuncionario.getModel();
        dados.setNumRows(0);
        
        for (Funcionario c : lista){
            dados.addRow(new Object[]{
                c.getId(),
                c.getLogin(),
                c.getSenha(),
                c.getNiveldeacesso(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEndereco(),
                c.getCep(),
                c.getCidade(),
                c.getUf(),
                c.getNumero(),
                c.getBairro()
            });
        }
    }
