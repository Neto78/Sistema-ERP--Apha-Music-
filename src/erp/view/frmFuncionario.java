        obj.setNiveldeacesso(cbNivel.getSelectedItem().toString()); 
        obj.setNome(tfNome.getText());
        obj.setRg(tfRg.getText());
        obj.setCpf(tfCPF.getText());
        obj.setEndereco(tfEndereco.getText());
        obj.setCep(tfCep.getText());
